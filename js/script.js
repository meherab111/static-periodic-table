const PeriodicElement = (atomicNo, elementName, elementFullName) => {
  const element = document.createElement("div");
  element.classList.add("element");
  element.innerHTML = `${atomicNo} ${elementName}`;

  element.setAttribute("data-atomic-number", atomicNo);
  element.setAttribute("data-element-name", elementFullName);

  return element;
};

const ElementClick = (event) => {
  const target = event.target;
  if (target.classList.contains("element")) {
    const atomicNumber = target.getAttribute("data-atomic-number");
    const elementFullName = target.getAttribute("data-element-name");
    alert(
      `Atomic No - ${atomicNumber}\nElement Full Name - ${elementFullName}`
    );
  }
};

const executeFunc = () => {
  const mainBody = document.querySelector(".mainBody");

  for (let i = 1; i <= 118; i++) {
    const element = PeriodicElement(
      i,
      `${arrElements[i]}`,
      `${arrElemFullName[i]}`
    );
    mainBody.append(element);
  }
  mainBody.addEventListener("click", ElementClick);
};

executeFunc();
